/* 
    Lecture note example - Variables (String, int, double)
*/

class LectureVariables{
    public static void main(String args[]) {
        
        String name;
        name = new String("Telapia");
        
        int apples;
        apples = 27;
        
        double cost; 
        cost = 78.49;
        
        System.out.println(name + " lives in Beverly Hills.");
        System.out.println(name + " has " + apples + " bathrooms in her house.");
        System.out.println(name + " with his " + apples + " bathrooms is $" + cost + " in debt ");
	}
}